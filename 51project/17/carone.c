//两个电机反转，前进
void goForward()
{
	left_con1A = 1;
	left_con2A = 0;
	
	right_con1A = 1;	
	right_con2A = 0;
}
 
//两个电机正转，后退
void goBack()
{
	left_con1A = 0;
	left_con2A = 1;
	
	right_con1A = 0;	
	right_con2A = 1;
}
 
//两个电机不转，停止
void goStop()
{
	left_con1A = 0;
	left_con2A = 0;
	
	right_con1A = 0;	
	right_con2A = 0;
}
 
//左电机不转，右电机反转，左转
void goLeft()
{
	left_con1A = 0;
	left_con2A = 0;
	
	right_con1A = 1;	
	right_con2A = 0;
}
 
//左电机反转，右电机不转，右转
void goRight()
{
	left_con1A = 1;
	left_con2A = 0;
 
	right_con1A = 0;	
	right_con2A = 0;
}
 
//循迹模式
void tracingMode()
{	
	if(tracingLeft == 0 && tracingRight == 0){  
		goForward();
	}
 
	if(tracingLeft == 0 && tracingRight == 1){
		goRight();
	}
 
	if(tracingLeft == 1 && tracingRight == 0){
		goLeft();
	}
 
	if(tracingLeft == 1 && tracingRight == 1){
		goStop();
	}
}
 
//跟随模式
void followMode()
{	
	if(followLeft == 0 && followRight == 0){  //
		goForward();
	}
 
	if(followLeft == 0 && followRight == 1){
		goRight();
	}
 
	if(followLeft == 1 && followRight == 0){
		goLeft();
	}
 
	if(followLeft == 1 && followRight == 1){
		goStop();
	}
}
 
//避障模式
void avoidMode()
{
	sg90Middle();
    Delay300ms();
    middleDistance = getDistance();
    if(middleDistance > 35){
        goForward();
    }else{
        goStop();
 
        sg90Left();
        Delay300ms();
        leftDistance = getDistance();
 
        sg90Middle();
        Delay300ms();
 
        sg90Right();
        Delay300ms();
        rightDistance = getDistance();
 
        if(leftDistance < 15 && rightDistance < 15){
            goBack();
            Delay500ms();
            goStop();
        }else{
 
            if(leftDistance > rightDistance){
                goLeft();
                Delay500ms();
                goStop();
            }
 
            if(rightDistance > leftDistance){
                goRight();
                Delay500ms();
                goStop();
            }
        }
    }
}
 
