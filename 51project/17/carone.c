//sg90
void timer0Init()
{
	//设置定时器0为16为计时模式
	TMOD &=0xF0; 
	TMOD |=0x01; 
 
	//设置定时器0定时时间为0.5ms
	TH0 = 0xFE;
	TL0 = 0x33;
 
	TR0 = 1;  //定时器0开始计时
	TF0 = 0;  //不执行定时器0爆表时导致的中断
 
	ET0 = 1;  //定时器0中断开关
	EA = 1;  //总中断开关
}
 
void sg90Left()
{
	angle = 5;  //180°
	if(angleBack != angle){  
		cnt = 0;
	}
	angleBack = angle;
	Delay100ms();
} 
 
