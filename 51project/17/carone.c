{
	sda = 0;
	scl = 1;
	sda = 1;
	Delay5us();
	sda = 0;
	Delay5us();
	scl = 0;
}
 
//IIC终止信号
void IIC_stop()
{
	scl = 0;
	sda = 0;
	scl = 1;
	Delay5us();
	sda = 1;
	Delay5us();
	sda = 0;
}
 
//IIC的ACK应答信号
char IIC_ack()
{
	char flag;
 
	scl = 0;
	sda = 1;  //在时钟脉冲9期间释放数据线
	Delay5us();  //延时5微秒后，为读取sda数据做准备
 
	scl = 1;  
	Delay5us();  
	flag = sda;  //读取数据线,0为应答
	Delay5us();  
	
	scl = 0;
	Delay5us();
	return flag;
}
 
//IIC发送一个字节
void IIC_sendByte(char myData)
{
	int i;
	for ( i = 0; i < 8; i++){
		//发生数据翻转，选择即将发送的是0还是1
		scl = 0;
		sda = myData & 0x80;  //获取需要发送字节的最高位到SDA
		Delay5us();  //数据建立时间
		
		//开始发送数据
		scl = 1;
		Delay5us();  //数据发送时间
		
		scl = 0;  //发送完毕拉低，等待下1bit数据的传输
		Delay5us();
		
		myData = myData << 1;
	}	
}
 
//OLED写入一条指令
void oledWriteCmd(char writeCmd)
{
	IIC_start();
	IIC_sendByte(0x78);  //选择一个OLED屏，写模式
	IIC_ack();
	IIC_sendByte(0x00);  //写入命令，D/C位为0
	IIC_ack();
	IIC_sendByte(writeCmd);
	IIC_ack();
	IIC_stop();
}
 
//OLED写入一个数据
void oledWriteData(char writeData)
{
	IIC_start();
	IIC_sendByte(0x78);  //选择一个OLED屏，写模式
	IIC_ack();
	IIC_sendByte(0x40);  //写入命令，D/C位为0
	IIC_ack();
	IIC_sendByte(writeData);
	IIC_ack();
	IIC_stop();
}
 
